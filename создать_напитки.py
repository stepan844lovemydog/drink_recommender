# -*- coding: utf-8 -*-
import pandas as pd

# Загружаем твой файл
df = pd.read_csv('as41 - Ответы на форму (1).csv', encoding='utf-8')

# --- ЛОГИКА НАЗНАЧЕНИЯ НАПИТКОВ ---
def get_drink(row):
    try:
        # Если хочет бодриться и кофеин обязателен
        if row['Что бы вы хотели прямо сейчас?'] == 'Бодрящее' and row['Кофеин для вас—это…'] == 'Обязательно':
            return 'Классический эспрессо'
        # Если хочет расслабляться и любит травяной чай
        if row['Что бы вы хотели прямо сейчас?'] == 'Расслабляющее' and 'Травяной' in str(row['Любите ли вы чай?']):
            return 'Мятный травяной чай'
        # Если хочет освежиться и сильно газированный
        if row['Что бы вы хотели прямо сейчас?'] == 'Освежающее' and row['Газированный напиток?'] == 'Да, сильно газированный':
            return 'Манго-маракуйя лимонад'
        # Если любит ягодные ноты и сладкий
        if 'Ягодные ноты' in str(row['Вам больше нравится:']) and row['Какой уровень сладости вам комфортен?'] in ['Сладкий', 'Умеренно сладкий']:
            return 'Ягодный фреш'
        # Если любит ореховые ноты и хочет бодриться
        if 'Ореховые ноты' in str(row['Вам больше нравится:']) and row['Что бы вы хотели прямо сейчас?'] in ['Бодрящее', 'Освежающее']:
            return 'Ореховый латте'
        # Если любит молочный улун и хочет расслабиться
        if row['Любите ли вы чай?'] == 'Молочный улун' and row['Что бы вы хотели прямо сейчас?'] == 'Расслабляющее':
            return 'Молочный улун с мёдом'
        # Если выбрал растительное молоко и цветочные ноты
        if 'Растительное' in str(row['Какое молоко вы предпочитаете?']) and 'Цветочные ноты' in str(row['Вам больше нравится:']):
            return 'Лавандовый раф'
        # Если без молока и любит фруктовые ноты
        if row['Какое молоко вы предпочитаете?'] == 'Без молока' and 'Фруктовые ноты' in str(row['Вам больше нравится:']):
            return 'Грейпфрутовый сок-сорбет'
        # Если любит экзотику и сладкие специи
        if 'Люблю экзотику' in str(row['Как вы относитесь к необычным сочетаниям?']) and 'Сладкие специи' in str(row['Вам больше нравится:']):
            return 'Мокко с перцем чили'
        # Если любит классику и черный чай
        if 'Только классика' in str(row['Как вы относитесь к необычным сочетаниям?']) and row['Любите ли вы чай?'] == 'Чёрный':
            return 'Чай с бергамотом и сливками'
        # Всем остальным — по умолчанию
        return 'Освежающий лимонад'
    except Exception as e:
        return 'Ошибка'

# Применяем логику к каждой строке
df['Напиток'] = df.apply(get_drink, axis=1)

# СОХРАНЯЕМ С РАЗДЕЛИТЕЛЕМ "ТОЧКА С ЗАПЯТОЙ" (для Excel)
df.to_csv('готовый_файл_с_напитками_FINAL.csv', index=False, sep=';', encoding='utf-8-sig')

