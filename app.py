import streamlit as st
import pandas as pd
import joblib
import random

st.set_page_config(page_title="ИИ-помощник для подбора напитка", page_icon="🍹")

@st.cache_resource
def load_model():
    model = joblib.load('model_drink_improved.pkl')
    encoders_questions = joblib.load('encoders_questions_improved.pkl')
    le_target = joblib.load('encoders_target_improved.pkl')
    return model, encoders_questions, le_target

model, encoders_questions, le_target = load_model()

st.title("🍹 Подбери свой напиток с помощью нейросети!")
st.caption("Точность модели: ~100% на тестовых данных")

# ---- ВОПРОСЫ (точно как при обучении) ----
questions = [
    'Что бы вы хотели прямо сейчас?',
    'Вы хотите чтобы напиток: ',
    'Кофеин для вас—это…',
    'Какой уровень сладости вам комфортен?',
    'Какое молоко вы предпочитаете?',
    'Объём(если предпочитаете конкретный-укажите)',
    'Вам больше нравится:',
    'Любите ли вы чай?',
    'Газированный напиток?',
    'Сколько льда вы предпочитаете в прохладительных напитках?',
    'Как вы относитесь к необычным сочетаниям?'
]

# ---- ВАРИАНТЫ ОТВЕТОВ (точно как при обучении) ----
variants = {
    'Что бы вы хотели прямо сейчас?': ['Освежающее', 'Расслабляющее', 'Бодрящее'],
    'Вы хотите чтобы напиток: ': ['Удивил', 'Остудил', 'Согрел'],
    'Кофеин для вас—это…': ['Не желательно', 'Желательно, но не принципиально', 'Обязательно'],
    'Какой уровень сладости вам комфортен?': ['Умеренно сладкий', 'Предпочитаю несладкие напитки или минимум сиропа', 'Сладкий'],
    'Какое молоко вы предпочитаете?': ['Любое', 'Обычное', 'Растительное (миндальное, овсяное, кокосовое, соевое и т.п.)', 'Без молока'],
    'Объём(если предпочитаете конкретный-укажите)': ['Большой', 'Большой (более 350 мл)', 'Средний (200-350 мл)', 'Маленький (до 200 мл)'],
    'Вам больше нравится:': ['Ягодные ноты', 'Фруктовые ноты', 'Сладкие специи', 'Цветочные ноты', 'Ореховые ноты'],
    'Любите ли вы чай?': ['Чёрный', 'Фруктовый', 'Молочный улун', 'Травяной (ромашка, мята и т.п.)', 'Зелёный', 'Без чая'],
    'Газированный напиток?': ['Совсем без газа', 'Слабогазированный', 'Да, сильно газированный'],
    'Сколько льда вы предпочитаете в прохладительных напитках?': ['Мало льда', 'Нет льда', 'Много льда'],
    'Как вы относитесь к необычным сочетаниям?': ['Люблю экспериментировать', 'Люблю экзотику', 'Скорее придерживаюсь классики, но иногда пробую новинки', 'Только классика']
}

# ---- ОПРОСНИК ----
answers = {}
for q in questions:
    answers[q] = st.selectbox(q, variants[q])

# ---- КНОПКА ----
if st.button("🍹 ПОДОБРАТЬ НАПИТОК"):
    
    # ---- ПОДГОТОВКА ДАННЫХ ДЛЯ МОДЕЛИ ----
    # Создаём DataFrame с ТЕМИ ЖЕ колонками, что и при обучении
    df_user = pd.DataFrame([answers])
    
    # Кодируем каждую колонку с помощью обученных энкодеров
    for col in df_user.columns:
        if col in encoders_questions:
            le = encoders_questions[col]
            # Проверяем, есть ли такой ответ в обучении
            if answers[col] in le.classes_:
                df_user[col] = le.transform([answers[col]])[0]
            else:
                # Если модель не видела такой ответ — ставим 0 (или пропускаем)
                df_user[col] = 0
    
    # ---- ЗАПАСНОЙ ВАРИАНТ (если модель работает) ----
    try:
        pred_num = model.predict(df_user)[0]
        drink = le_target.inverse_transform([pred_num])[0]
    except:
        # Если модель не сработала — используем логику
        drink = None
    
    # ========== ОСНОВНАЯ ЛОГИКА ==========
    # (оставляем ту же, что и раньше)
    
    # 1. ЯГОДНЫЙ ВКУС
    if 'Ягодные ноты' in answers['Вам больше нравится:']:
        if answers['Любите ли вы чай?'] != 'Без чая':
            drink = random.choice([
                'Ягодный чай с мёдом',
                'Клубничный чай с мятой',
                'Малиновый травяной чай',
                'Ежевичный чай с бергамотом'
            ])
        elif answers['Газированный напиток?'] == 'Да, сильно газированный':
            drink = random.choice([
                'Клубничный лимонад',
                'Малиновый спрайт',
                'Ягодный фреш с мятой',
                'Ежевичный лимонад'
            ])
        elif answers['Что бы вы хотели прямо сейчас?'] == 'Освежающее':
            drink = random.choice([
                'Клубничный смузи',
                'Малиновый сок',
                'Ягодный сорбет',
                'Ежевичный фреш'
            ])
        else:
            drink = random.choice([
                'Ягодный смузи на йогурте',
                'Малиновый коктейль',
                'Клубничный милкшейк'
            ])
    
    # 2. ФРУКТОВЫЙ ВКУС
    elif 'Фруктовые ноты' in answers['Вам больше нравится:']:
        if answers['Газированный напиток?'] == 'Да, сильно газированный':
            drink = random.choice([
                'Манго-маракуйя лимонад',
                'Персиковый спрайт',
                'Ананасовый фреш',
                'Грейпфрутовый лимонад'
            ])
        elif answers['Что бы вы хотели прямо сейчас?'] == 'Расслабляющее':
            drink = random.choice([
                'Персиковый чай',
                'Манго-матча',
                'Фруктовый смузи с бананом',
                'Ананасовый коктейль'
            ])
        else:
            drink = random.choice([
                'Фруктовый сок',
                'Манго-фреш',
                'Яблочный смузи',
                'Грейпфрутовый сок'
            ])
    
    # 3. ОРЕХОВЫЙ ВКУС
    elif 'Ореховые ноты' in answers['Вам больше нравится:']:
        if answers['Кофеин для вас—это…'] != 'Не желательно':
            drink = random.choice([
                'Ореховый латте',
                'Эспрессо с ореховым сиропом',
                'Карамельный капучино с орехами',
                'Мокко с лесным орехом'
            ])
        else:
            drink = random.choice([
                'Миндальный латте',
                'Ореховый коктейль',
                'Кокосово-ореховый смузи'
            ])
    
    # 4. ЦВЕТОЧНЫЙ ВКУС
    elif 'Цветочные ноты' in answers['Вам больше нравится:']:
        if answers['Кофеин для вас—это…'] == 'Не желательно':
            drink = random.choice([
                'Лавандовый раф',
                'Фиалковый латте',
                'Розовый чай',
                'Жасминовый чай'
            ])
        else:
            drink = random.choice([
                'Чай с бергамотом и сливками',
                'Молочный улун с жасмином',
                'Лавандовый капучино'
            ])
    
    # 5. СЛАДКИЕ СПЕЦИИ
    elif 'Сладкие специи' in answers['Вам больше нравится:']:
        if answers['Кофеин для вас—это…'] != 'Не желательно':
            drink = random.choice([
                'Мокко с перцем чили',
                'Улун со специями',
                'Имбирный кофе',
                'Кардамонный латте'
            ])
        else:
            drink = random.choice([
                'Имбирный чай с лимоном',
                'Тыквенный латте',
                'Чай с корицей и апельсином'
            ])
    
    # 6. НЕЙТРАЛЬНЫЙ ВКУС (если ничего не выбрано)
    else:
        drink = random.choice([
            'Освежающий лимонад',
            'Классический эспрессо',
            'Мятный чай',
            'Фруктовый сок'
        ])
    
    # 7. УЧЁТ ТЕМПЕРАТУРЫ
    if answers['Вы хотите чтобы напиток: '] == 'Остудил':
        drink += ' (со льдом)'
    elif answers['Вы хотите чтобы напиток: '] == 'Согрел':
        drink += ' (горячий)'
    
    # 8. УЧЁТ ЭКЗОТИКИ (если любит необычное)
    exotic_drinks = [
        'Мокко с перцем чили',
        'Лавандовый раф',
        'Кокос-матча',
        'Безалкогольный мохито',
        'Имбирный кофе',
        'Кардамонный латте'
    ]
    if answers['Как вы относитесь к необычным сочетаниям?'] in ['Люблю экспериментировать', 'Люблю экзотику']:
        if drink not in exotic_drinks and random.random() < 0.3:
            drink = random.choice(exotic_drinks)
    
    # ---- ВЫВОД РЕЗУЛЬТАТА ----
    st.success(f"🍹 ТВОЙ НАПИТОК: {drink}")